package com.whippy.poker.state.beans;

public enum Suit {


        //~ ----------------------------------------------------------------------------------------------------------------
        //~ Enum constants
        //~ ----------------------------------------------------------------------------------------------------------------

        DIAMONDS(0), HEARTS(1), CLUBS(2), SPADES(3);


        private int numericValue;

        //~ ----------------------------------------------------------------------------------------------------------------
        //~ Constructors
        //~ ----------------------------------------------------------------------------------------------------------------

        private Suit(int numericValue) {
                this.numericValue = numericValue;
        }

        //~ ----------------------------------------------------------------------------------------------------------------
        //~ Methods
        //~ ----------------------------------------------------------------------------------------------------------------

        public int getNumericValue() {
                return numericValue;
        }

}
