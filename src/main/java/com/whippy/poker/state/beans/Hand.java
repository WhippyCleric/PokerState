package com.whippy.poker.state.beans;

public class Hand {

	//~ ----------------------------------------------------------------------------------------------------------------
	//~ Instance fields 
	//~ ----------------------------------------------------------------------------------------------------------------

	private Card[] hand;

	//~ ----------------------------------------------------------------------------------------------------------------
	//~ Constructors 
	//~ ----------------------------------------------------------------------------------------------------------------

	public Hand(Card card1, Card card2) {
		hand = new Card[2];
		hand[0] = card1;
		hand[1] = card2;
	}

	public Card[] getCards(){
		return this.hand;
	}
}
